export const getMockAIResponse = async (message: string): Promise<string> => {
  await new Promise((resolve) => setTimeout(resolve, 700));

  const text = message.toLowerCase();

  if (text.includes('привет')) {
    return 'Привет! Я AI-ассистент RAONE. Помогу подобрать одежду, стиль, размеры и образ для примерочной.';
  }

  if (text.includes('погода')) {
    return 'Для прохладной погоды я бы рекомендовал сочетание: верхняя одежда, базовый верх, удобные брюки и закрытую обувь.';
  }

  if (text.includes('стиль')) {
    return 'Я могу помочь подобрать casual, classic, streetwear, sporty или smart casual стиль. Напиши, какой образ тебе нужен.';
  }

  if (text.includes('размер')) {
    return 'Для точного выбора размера лучше ориентироваться на параметры аватара и таблицу размеров товара.';
  }

  if (text.includes('примероч')) {
    return 'В виртуальной примерочной ты можешь собрать образ из нескольких вещей и посмотреть, как он будет выглядеть на аватаре.';
  }

  if (text.includes('каталог')) {
    return 'В каталоге можно использовать поиск, фильтрацию и сортировку, а затем добавить товар в примерочную.';
  }

  if (text.includes('что ты умеешь')) {
    return 'Я умею помогать с подбором одежды, стилем, рекомендациями по погоде, размерами и навигацией по приложению.';
  }

  if (text.includes('муж')) {
    return 'Для мужского образа могу предложить базовую футболку, брюки, куртку и кроссовки. Если хочешь, подберу более конкретный стиль.';
  }

  if (text.includes('жен')) {
    return 'Для женского образа могу предложить платье или сочетание рубашки, юбки и обуви в подходящей цветовой гамме.';
  }

  return 'Я могу помочь с подбором одежды, стилем, размерами, погодными рекомендациями и использованием примерочной.';
};